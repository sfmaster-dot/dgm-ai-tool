// 1) Origin 허용목록 — 브라우저 핫링킹/타 사이트 임베드 차단 (curl 등 헤더 위조는 못 막음 → 아래 IP 스로틀과 병행)
// 2) IP 인메모리 슬라이딩 윈도우 — 캐주얼 남용/봇 폭주 완화. 인스턴스별·콜드스타트 시 리셋되는 경량 방어.
//    트래픽이 커지면 Upstash Redis 등 공유 저장소로 승격 권장(README 참조).
const ALLOWED_ORIGINS = (process.env.ALLOWED_ORIGINS ||
  'https://danggum.net,https://www.danggum.net').split(',').map(s => s.trim()).filter(Boolean);
const RATE_MAX = Number(process.env.RATE_MAX || 15);        // 윈도우당 최대 요청
const RATE_WINDOW_MS = Number(process.env.RATE_WINDOW_MS || 60 * 60 * 1000); // 1시간

const _hits = new Map(); // ip -> number[] (타임스탬프)
function rateLimited(ip) {
  const now = Date.now();
  const arr = (_hits.get(ip) || []).filter(t => now - t < RATE_WINDOW_MS);
  arr.push(now);
  _hits.set(ip, arr);
  if (_hits.size > 5000) { // 메모리 상한 — 오래된 IP 정리
    for (const [k, v] of _hits) { if (!v.length || now - v[v.length - 1] > RATE_WINDOW_MS) _hits.delete(k); }
  }
  return arr.length > RATE_MAX;
}

function originAllowed(req) {
  // 허용목록이 비어 있으면 검사 생략(로컬 개발 편의). 프로덕션에선 ALLOWED_ORIGINS 설정 권장.
  if (!ALLOWED_ORIGINS.length) return true;
  const origin = req.headers.origin || '';
  const referer = req.headers.referer || '';
  return ALLOWED_ORIGINS.some(o => origin === o || referer.startsWith(o));
}

